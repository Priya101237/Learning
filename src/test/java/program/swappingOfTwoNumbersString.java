package program;

public class swappingOfTwoNumbersString {

	public static void main(String[] args) {
	
		String s1="Automation";
		String s2="Tester";
		System.out.println("before of swapping s1:" +s1);
		System.out.println("before of swapping  s2:" +s2);
		
		String temp=s1;
		s1=s2;
		s2=temp;
		
		System.out.println("After of swapping s1 :" +s1);
		System.out.println("After of swapping s2 :" +s2);
		
		
		
		

	}

}
